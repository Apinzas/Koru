package com.tektonlabs.android.refugeapp.data.db.models;

import io.realm.RealmObject;

public class AnswerDb extends RealmObject {

    private Integer id;

    private String name;

    private Boolean withValue;

    private String createdAt;

    private String updatedAt;

    // For get the selected answers
    private boolean isSelected;
    private String answerValue;

    private int shelterId;

    public int getShelterId() {
        return shelterId;
    }

    public void setShelterId(int shelterId) {
        this.shelterId = shelterId;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Boolean getWithValue() {
        return withValue;
    }

    public void setWithValue(Boolean withValue) {
        this.withValue = withValue;
    }

    public String getAnswerValue() {
        return answerValue;
    }

    public void setAnswerValue(String answerValue) {
        this.answerValue = answerValue;
    }

    public boolean isSelected() {
        return isSelected;
    }

    public void setSelected(boolean selected) {
        isSelected = selected;
    }
}
